function changeStyle_black(){
    document.getElementById('postagem').className = 'postagem2';
    document.getElementById('conteudo-lateral').className = 'conteudo-lateral2'
    document.getElementById('categorias').className = 'conteudo-lateral2'
    document.getElementById('categorias').className = 'h3-black'
    document.getElementById('postagens-recentes').className = 'h3-black'
}
function changeStyle_white(){
    document.getElementById('postagem').className = 'postagem';
    document.getElementById('conteudo-lateral').className = 'conteudo-lateral'
    document.getElementById('categorias').className = 'conteudo-lateral'
    document.getElementById('categorias').className = 'h3-white'
    document.getElementById('postagens-recentes').className = 'h3-white'   
}